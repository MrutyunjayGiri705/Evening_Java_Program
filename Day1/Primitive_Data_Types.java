public class Primitive_Data_Types {
    public static void main(String[] args) {
        byte age=20;
        char A='B';
        float B=7.6f;
        double C=4.660D;
        long D=33333333333333L;
        boolean E=true;
        System.out.println(age+"  "+A+"  "+"  "+"  "+B+"  "+C+"  "+D+"   "+E);

    }
    
}
