public class Coditionals {
    public static void main(String[] args) {
        int age=20;
        if (age>18) {
            System.out.println("Yes boy you can drive!");
            
        }
        else{
            System.out.println("Yes boy you cannot drive!");
        }
        boolean a=true;
        boolean b= false;
        if (a && b) {
            System.out.println("Y");
            
        }
        else{
             System.out.println("N");
        }
        if (a || b) {
            System.out.println("Y");
            
        }
        else{
             System.out.println("N");
        }
        if (a & b) {
            System.out.println("Y");
            
        }
        else{
             System.out.println("N");
        }
        if (a | b) {
            System.out.println("Y");
            
        }
        else{
             System.out.println("N");
        }
    }
    
}
